test("reset from random", function() {
	var game = GameOfLife();
	game.reset(true);

	equal(game.board.length, 50);
	equal(game.board[0].length, 50)
});

test("reset from not random", function() {
	var game = GameOfLife();
	game.reset(false);

	for (var j = 0; j<50; j++){
		for (var i=0;i<50;i++){
			equal(game.board[j][i], 0);
		}
	}

	equal(game.board.length, 50);
	equal(game.board[0].length, 50)
});

